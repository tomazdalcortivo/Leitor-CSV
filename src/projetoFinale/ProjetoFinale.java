    String linha = "";
    String produtoComMaiorVenda = null;
    int quantidadeMaisVendida = 0;

    ArrayList<Mercadoria> produtos = new ArrayList<>();

    private void processarArquivoCSV(File arquivoSelecionado) {
        try (BufferedReader leitor = new BufferedReader(new FileReader(arquivoSelecionado))) {
            boolean primeiraLinha = true;

            while ((linha = leitor.readLine()) != null) {
                if (primeiraLinha) {
                    primeiraLinha = false;
                    continue;
                }

                String[] colunas = linha.split(";");
                String colunaProduto = colunas[4];
                int colunaQuantidade = Integer.parseInt(colunas[5]);

                Mercadoria mercadoria = null;
                for (int i = 0; i < produtos.size(); i++) {
                    if (produtos.get(i).getCategoriaProduto().equals(colunaProduto)) {
                        mercadoria = produtos.get(i);
                        break;
                    }
                }
                if (mercadoria != null) {
                    mercadoria.acomuladorQuantidade(colunaQuantidade);
                } else {
                    mercadoria = new Mercadoria(colunaProduto, colunaQuantidade);
                    produtos.add(mercadoria);
                }

                for (int i = 0; i < produtos.size(); i++) {

//                  gera o indice do array
                    Mercadoria mercadoriaAtual = produtos.get(i);
                    if (mercadoriaAtual.getQuantidade() > quantidadeMaisVendida) {
                        quantidadeMaisVendida = mercadoria.getQuantidade();
                        produtoComMaiorVenda = mercadoria.getCategoriaProduto();
                    }
                }
                jTextField4.setText(produtoComMaiorVenda + " / " + quantidadeMaisVendida);
            }

        } catch (IOException e) {
            e.printStackTrace();
        }
    }

